"use strict";
// store a person name in a variable
let personname = "Muhammad Khalid Hussain";
// Print Person name in UpperCase
console.log("LowerCase:", personname.toLowerCase());
// Print Person name in UpperCase
console.log("UpperCase:", personname.toUpperCase());
// Print Person name in TitleCase
console.log("titlecase:", personname.totitlecase());
